    <h2 style="margin-bottom: 25px;text-align: center;">Política de Compra, Venda e Devolução</h2>
    <h5>Nossa política</h5>
    <p>Enxovais DuLar tem muito respeito por seus clientes, e para manter a credibilidade e a transparência
        nessa relação, possuímos uma política de trocas e devoluções com base no Código de Defesa do
        Consumidor e nas necessidades dos seus clientes. Este documento estabelece os procedimentos e
        normas de compra, venda e devoluções dos produtos adquiridos no nosso site.</p>
    <h5>Processo de COMPRA E VENDA</h5>
    <p>O processo de aquisição de um produto no site Enxovais DuLar é composto pelas seguintes etapas:</p>
    <h6>1ª – Cliente Revendedora</h6>
    <p>Após preenchimento do campo de cadastro no site, será analisado, pois a empresa possui Rotas de
        Vendas e não atenderá pelo site os locais onde possui Rotas;</p>
    <p>Para as Revendedoras, que obterão diferença nos preços, o valor mínimo de compra para o envio
        das mercadorias é de R$ 600,00;</p>
    <p>Após confirmada a compra e o pagamento, a mercadoria será enviada (correios ou transportadora)
        que irá estipular o prazo para entrega, e ficará por conta da Cliente o Frete que será pago na retirada da
        mercadoria;</p>
    <h6>2ª– Cliente Consumidora</h6>
    <p>Com o preenchimento do campo de cadastro no site, em locais onde a empresa atua através de Rotas
        de Vendas, a mercadoria não poderá ser enviada. O pedido deverá ser com valor igual ou superior a
        R$350,00, e após confirmação do pagamento, a compra estará concluída. O Frete (correios ou
        transportadora) será por conta da Cliente e ser pago na retirada da mercadoria. O prazo de entrega
        dependerá dos agentes acima colocados.</p>
    <p>Enxovais DuLar esclarece que a análise do PEDIDO DE COMPRA é medida necessária para evitar
        fraudes em nome de seus USUÁRIOS, o que poderia gerar grandes prejuízos aos mesmos. Assim sendo,
        preferimos adotar esta medida de segurança a permitir que terceiros lesem nossos clientes.</p>
    <p>Nos locais onde a empresa possui Rotas de Vendas, caso haja interesse, entrar em contato para que a
        visita de um de nossos representantes possa ser agendada.</p>
    <h5>O preço correto da COMPRA E VENDA</h5>
    <p>O preço para Clientes Revendedoras é diferenciado ao de Clientes Consumidoras e o valor do Frete sofre
        alterações em conformidade com a localização da entrega.</p>
    <h5>Política de Pagamento</h5>
    <p>As compras efetuadas exclusivamente pelo site Enxovais DuLar poderão ser pagas por meio de Depósito
        Bancário ou Boleto que será enviado por e-mail e pago na data determinada, e o prazo de entrega deverá
        ser a partir da confirmação do pagamento.</p>
    <h6>Boleto Bancário:</h6>
    <p>Para compras à vista por Boleto Bancário, o pagamento pode ser efetuado em qualquer banco. Nesta
        modalidade, o mesmo deverá ser feito em uma única parcela no valor total da compra.</p>
    <h5>Devolução da mercadoria após a entrega</h5>
    <p>Para desistir de um produto após a entrega, o cliente deve entrar em contato com a empresa, observando
        as seguintes condições: O prazo para desistir da compra do produto é de até 07 (sete) dias corridos, a
        contar da data do recebimento. O produto deverá conter defeito na fabricação e ser encaminhado na
        embalagem original, sem indícios de uso, ou que tenha sido lavado, e acompanhada de nota fiscal. A
        mercadoria deverá ser despachada no mesmo endereço que foi efetuada a entrega. O prazo para
        recolhimento será informado pela empresa. O cliente é responsável pelas condições do produto a ser
        devolvido. Caso alguma das condições acima não seja verificada, a solicitação de devolução poderá não
        ser acatada e o cliente será comunicado por e-mail ou por telefone.</p>
